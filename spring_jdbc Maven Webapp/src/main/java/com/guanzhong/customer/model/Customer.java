package com.guanzhong.customer.model;

public class Customer
{
	int cusId;
	String name;
	int age;
	
	public int getCusId()
	{
		return cusId;
	}
	public void setCusId(int cusId)
	{
		this.cusId = cusId;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public int getAge()
	{
		return age;
	}
	public void setAge(int age)
	{
		this.age = age;
	}
	
	
	

}
